let Account = function (id, nickname, password, name, lastname, email, phone) {
    this.id = id;
    this.nickname = nickname;
    this.password = password;
    this.name = name;
    this.lastname = lastname;
    this.email = email;
    this.phone = phone;
}

let SharedAccount = function(id, nickname, name, lastname, email, phone) {
    this.id = id;
    this.nickname = nickname;
    this.name = name;
    this.lastname = lastname;
    this.email = email;
    this.phone = phone;
}

